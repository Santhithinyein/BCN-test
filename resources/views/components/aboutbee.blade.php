ပျားရည်သည် လူကြိုက်များသောအာဟာရတစ်မျိုးဖြစ်သည်။ ယင်းကို သစ်တောများ၌ ရှာဖွေယူကြရ၏။ ယခုအခါ၌ ပျားရည်အလို့ငှာ ပျားကို စီးပွားဖြစ် မွေးမြူကြသည်။ ပျားအုံမှရသော ပျားဖယောင်းကို ဖယောင်းတိုင်များ ပြုလုပ်၍လည်း သုံးကြ၏။ လိမ်းဆေးများ ဖော်စပ်ရာ၌လည်း ပျားဖယောင်းများကိုသုံကြသည်။ ထို့အပြင် ပျားတို့၏ လူတို့အပေါ် အခြားအကျိုးပြုချက်တစ်ရပ်ကား သစ်သီးများ ပိုမိုထွက်စေခြင်းပင် ဖြစ်သည်။ ပျားတို့သည် တစ်ပင်မှတစ်ပင် ကူးလူးပျံသန်းလျက်ရှိကြရာ ကူးယှက် မျိုးတည်မှု ဖြစ်စေသည်။

