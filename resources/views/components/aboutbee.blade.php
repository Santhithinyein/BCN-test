                    <h3>ပျားများ၏ စိတ်ဝင်စားစရာကောင်းသောအချက်များ</h3>
                    <p>
                    
ကမ္ဘာကြီးရဲ့ တာဝန်တော်တော်များများကို ကျောပိုးထားတယ်ဆိုပြီး လူတိုင်းသိကြတဲ့ ပျားတွေဟာ စိုက်ပျိုးရေးကဏ္ဍမှာအကျိုးပြုရုံသာမက သူတို့ဆီကရတဲ့ ပျားရည်ဟာလည်း လူသားများအတွက် ကျန်းမာရေးအကျိုးကျေးဇူးများစွာရှိ၏။
တကယ်လို့ ပျားတွေသာ ကမ္ဘာကြီးပေါ်က လုံးဝ ပျောက်ကွယ်သွားမယ်ဆိုရင် လူသားတွေဟာ ဆက်လက်အသက်ရှင်ဖို့ ၄ နှစ်လောက်ပဲတတ်နိုင်တော့ပါမယ်တဲ့။(အဲ့ဒီအကြောင်းရင်းသီးသန့်ကြောင့်မဟုတ်ပေမဲ့ အဲ့ဒီကနေဆက်စပ်ဖြစ်လာနိုင်တဲ့အကျိုးဆက်တွေကြောင့်ပါ) ပျားများသည် လူသားတွေအတွက် မရှိမဖြစ်အကောင်လေးတွေဖြစ်ပါတယ်။ဒီလိုမျိုးကောင်းကျိုးတွေကို ထောက်ပံ့ပေးနေတဲ့ ပျားများတွင်ထူးခြားချက်များရှိ၏။အများကြီးရှိသည့်အနက်မှ အနည်းငယ် ဖော်ပြပေးချင်ပါသည်။
(၁)  ပျားတွေက ဝတ်မှုန်များကို သယ်ဆောင်ရန် သူတို့ရဲ့ နောက်ခြေထောက်မှာ corbicula လို့ခေါ်တဲ့ ဝတ်မှုန်သယ်ခြင်းတောင်းပါရှိပါတယ်။
(၂) ပျားတွေက  pheromones (ဖီရိုမုန်း) လို့ခေါ်တဲ့ ဟော်မုန်းလိုမျိုး ခန္ဓာကိုယ်စစ်ထုတ်ရည်တစ်မျိုးကို သုံးပြီး အချင်းချင်း အဆက်အသွယ်လုပ်ကြပါတယ်။
(၃) ပျားထီးတွေက သူတို့ရဲ့ ဘဝမှာ ပျားဘုရင်မနဲ့ တစ်ကြိမ်သာ မိတ်လိုက်ကြပြီး မိတ်လိုက်ပြီးနောက် အဲ့ဒီပျားထီး သေသွားပါတယ်။
(၄) ပျားဘုရင်မကတော့ များသောအားဖြင့် သူတို့ရဲ့ ဘဝမှာ သက်တမ်းတစ်ကြိမ်သာ မိတ်လိုက်ပါ တယ်။ အဲ့ဒီတစ်ကြိမ်အတွင်း ပျားထီးအကောင်ရေတော်တော်များများနဲ့ မိတ်လိုက်လိုက်တာဖြစ်ပြီး မိတ်လိုက်မှုပြုလုပ်တဲ့ကာလကတော့ တစ်ရက်ကနေ နှစ်ရက်ထိ ကြာနိုင်တယ်လို့ သိရပါတယ်။

